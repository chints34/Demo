
$INCLUDE(DELAYS.ASM)

SEVEN_SEG EQU P2
SEL BIT P0^7

LUT_SEVEN SEGMENT CODE
RSEG LUT_SEVEN
LUT: DB 40H,79H,24H,30H,19H,12H,02H,78H,00H,10H

CSEG AT 0
MAIN:
MOV DPTR,#LUT
MOV R1,#10


UP:
CLR SEL
CLR A
MOVC A,@A+DPTR
MOV SEVEN_SEG,A
ACALL DELAY1S

INC DPTR
DJNZ R1,UP
SJMP MAIN
END

$INCLUDE(DELAYS.ASM)
SW1 BIT P3.0
SW2 BIT P3.1

CSEG AT 0
MAIN: 	MOV SEG1,#40H
	MOV SEG2,#40H
	MOV DPTR,#SEG_LUT
	MOV R4,#0
AGAIN:	JNB SW2,AGAIN2
	JB SW1,AGAIN
	INC R4
	ACALL DISP_2_SEG
	JNB SW1,$
	CJNE R4,#60,AGAIN
	SJMP MAIN
AGAIN2:	JNB SW2,$
AGAIN3:	ACALL DISP_2_SEG
	ACALL DELAY1S
	DJNZ R4,AGAIN3
SJMP MAIN
END
	 
